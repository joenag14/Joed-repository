curso = 'Ultimate "python"'
print(curso)
# para imprimir un mensaje que tenga comillas, habran varias formas, una de ella es poniendo el mensaje global en una comilla simple (alt+39)
# y la palabra o palabras entre comillas usando comillas dobles.

curso = 'Ultimate "python"'
print(curso)
# La segunda manera sera utilizar un back slash \ (alt+92), este se debe agregar antes de las primeras comillas dobles y despues de la palabra
# o palabras entre comillas,  aunque para este ejemplo el sistema automaticamente lo corrgio.

curso = 'Ultimate \n"python"'
print(curso)
# Al agregar un back slash y una (n) minuscula antes de poner agregar la palabara entre comillas, esto hara que la palabra entre comillas sera agregada en una siguiente linea de texto.
# Basicamente, se puede utilizar esta opcion para que el texto despues del back slash + (n) se muestre en la siguiente linea.
